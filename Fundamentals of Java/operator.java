class OperatorsDemo{
       public static void main(String [] args){
	          int a =17, b=3;
			  int ans;
			  ans = a+b;
			  System.out.println(ans);
			  
			  ans = a-b;
			  System.out.println(ans);
			  
			  ans = a*b;
			  System.out.println(ans);
			  
			  ans = a/b;
			  System.out.println(ans);
		}
		}
		